package com.cpg.corejava.basics;

public class AssignmentCompoundOperators {

	public static void main(String[] args) 
	{
		int i = 10; // Initialization and Declaration of i
		int j = 20; // Initialization and Declaration of j
		j += i; // j = j+i
		System.out.println(j); //Utilization of j
		j -=i; //j= j-1;
		System.out.println(j); //Utilization of j
		j *=i; //j=j*i
		System.out.println(j); //Utilization of j
		j /=i; //j = j/i
		System.out.println(j); //Utilization of j
		j %= i; //j = j%i
		System.out.println(j); //Utilization of j

	}

}
