package com.cpg.corejava.basics;

public class RelationalOperators {

	public static void main(String[] args) 
	{
		int i = 10;
		int j = 20;
		
		System.out.println(i==j); // Equal to
		System.out.println(i!=j);
		System.out.println(i>j);
		System.out.println(i>=j);
		System.out.println(i<j);
		System.out.println(i<=j);
		

	}

}
