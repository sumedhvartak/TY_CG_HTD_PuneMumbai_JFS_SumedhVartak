package com.cpg.corejava.basics;

public class Variables {

	public static void main(String[] args) 
	{
		byte b;// declaration of byte
		b = -128;// Initialization of byte
		short s = 101;// declaration and initialization of short
		int i = 101;// declaration and initialization of integer
		float f = 101.015678856778899f; //declaration and initialization of float
		long l = 123345;  //declaration and initialization of long
		double d = 101.015678856778899; //declaration and initialization of double
		char c = 'A';  //declaration and initialization of character
		boolean q = true;  //declaration and initialization of boolean
		
		System.out.println(b);//utilization of byte
		System.out.println(s);//utilization of short
		System.out.println(i);//utilization of integer
		System.out.println(f);//utilization of float
		System.out.println(l);//utilization of long
		System.out.println(d);//utilization of double
		System.out.println(c);//utilization of character
		System.out.println(q);//utilization of boolean
		
		

	}

}
